#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	size_t			len;

	i = 0;
	len = ft_strlen(s);
	if (s == NULL)
		return ;
	while (*s != '\0' && i <= len)
		f(i++, s++);
}
